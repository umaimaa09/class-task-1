// Example Of String Data Type With Concatenation
var anycountry = ("Pakistan");
console.log("I Live in" + anycountry + ".");
// Example Of Number Data Type With Arithmetic Operation
var m = 5;
var n = 9;
console.log(m + n);
console.log(m - n);
console.log(m * n);
console.log(m / n);
console.log(m % n);
console.log(Math.pow(m, 4));
console.log(m * n / 7);
// Example Of Boolean Data Types
var t = true;
var f = false;
console.log("students have done their homework", t);
console.log("students has done their homework", f);
// Example Of Null Data Types
var number = null;
console.log(number);
// Example Of Undefined Data Types
var city;
console.log(city);
// Example Of Symbol Data Type
var x = Symbol('fullstop');
console.log(x);
//Example Of BigInt Data Type
var value = BigInt(904000);
console.log(value);
